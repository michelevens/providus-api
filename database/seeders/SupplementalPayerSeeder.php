/**
 * Supplemental payer catalog — 253 behavioral-health-relevant
 * payers that did not fit in the original 63-payer PayerSeeder.
 *
 * Source: was previously a client-side JS file at
 * providus-app/data/missing-payers-catalog.js that V1 merged into the
 * browser PAYER_CATALOG global at boot. V2 had no equivalent merge,
 * so /payers/<id> for any of these IDs (100+) returned 404.
 *
 * This seeder lifts the data into the database as the single source of
 * truth. V1 + V2 + any new tenant now see the full catalog by querying
 * /api/reference/payers — no client-side merge required.
 *
 * Idempotent: dedupes by slug (existing rows are skipped). Tags from
 * the JS source are dropped — the payers table has no tags column, and
 * V1's enrichPayerTags() applies them client-side from PAYER_TAG_MAP
 * based on name match.
 */
class SupplementalPayerSeeder extends Seeder
{
    public function run(): void
    {
        $existingSlugs = array_flip(DB::table('payers')->pluck('slug')->toArray());
        $inserted = 0; $skipped = 0;

        foreach ($this->payers() as $p) {
            if (isset($existingSlugs[$p['slug']])) { $skipped++; continue; }
            $p['states'] = json_encode($p['states']);
            $p['created_at'] = now();
            DB::table('payers')->insert($p);
            $inserted++;
        }

        $this->command->info("SupplementalPayerSeeder: inserted {$inserted}, skipped {$skipped}.");
    }

